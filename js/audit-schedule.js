const auditScheduleState = {
  loaded: false,
  loading: false,
  error: null,
  raw: null,
  filters: {
    search: "",
    months: [],
    gates: [],
    years: ["2026"]
  },
  quietRender: false,
  openFilter: null
};

const AUDIT_SCHEDULE_GATES = ["MS0", "MS1", "QG2", "QG3"];

function carregarAuditSchedule(force = false) {
  if (!force && (auditScheduleState.loaded || auditScheduleState.loading)) {
    return Promise.resolve(auditScheduleState);
  }

  auditScheduleState.loading = true;
  auditScheduleState.error = null;
  renderAuditSchedule();

  return firebase.firestore()
    .collection("auditSchedule")
    .doc("auditSchedule")
    .get()
    .then(doc => {
      auditScheduleState.loading = false;
      auditScheduleState.loaded = true;

      if (!doc.exists) {
        auditScheduleState.raw = null;
        return auditScheduleState;
      }

      const item = doc.data();
      auditScheduleState.raw = _parseAuditScheduleJson(item.jsonCompleto || item);
      return auditScheduleState;
    })
    .catch(error => {
      auditScheduleState.loading = false;
      auditScheduleState.loaded = true;
      auditScheduleState.error = error;
      auditScheduleState.raw = null;
      console.error("Audit schedule load error:", error);
      return auditScheduleState;
    })
    .finally(renderAuditSchedule);
}

function abrirAuditSchedule() {
  if (typeof carregarAudits === "function" && !auditState.loaded && !auditState.loading) {
    carregarAudits().then(renderAuditSchedule);
  }

  if (!auditScheduleState.loaded && !auditScheduleState.loading) {
    carregarAuditSchedule();
  } else {
    renderAuditSchedule();
  }
}

function renderAuditSchedule() {
  const section = document.getElementById("audit-schedule-section");
  if (!section) return;
  const shellClass =
    auditScheduleState.quietRender
      ? "audit-schedule-shell audit-schedule-shell-quiet"
      : "audit-schedule-shell";

  if (auditScheduleState.loading) {
    section.innerHTML = `
      <div class="${shellClass}">
        ${_renderAuditScheduleHeader(null)}
        ${_renderAuditScheduleLoading()}
      </div>
    `;
    return;
  }

  if (auditScheduleState.error) {
    section.innerHTML = `
      <div class="${shellClass}">
        ${_renderAuditScheduleHeader(null)}
        <section class="audit-schedule-panel">
          <div class="audit-schedule-empty">
            <i class="fa-solid fa-triangle-exclamation"></i>
            <strong>Audit schedule unavailable</strong>
            <span>Check Firestore document auditSchedule/auditSchedule.</span>
          </div>
        </section>
      </div>
    `;
    return;
  }

  const data = _normalizeAuditScheduleData(auditScheduleState.raw);

  if (!data.events.length) {
    section.innerHTML = `
      <div class="${shellClass}">
        ${_renderAuditScheduleHeader(data)}
        <section class="audit-schedule-panel">
          <div class="audit-schedule-empty">
            <i class="fa-regular fa-calendar"></i>
            <strong>No schedule JSON loaded yet</strong>
            <span>Create auditSchedule/auditSchedule with jsonCompleto generated by the PMO macro.</span>
          </div>
        </section>
      </div>
    `;
    _bindAuditScheduleEvents();
    return;
  }

  const filtered = _filterAuditScheduleData(data);
  const auditEvents = filtered.events.filter(event => event.isAuditGate);

  section.innerHTML = `
    <div class="${shellClass}">
      ${_renderAuditScheduleHeader(data, filtered.events)}
      ${_renderAuditScheduleControls(data)}
      ${_renderAuditScheduleCalendar(filtered.months)}
      ${_renderAuditScheduleQueueStrip(auditEvents)}
      ${_renderAuditScheduleGateMix(filtered.events)}
    </div>
  `;

  _bindAuditScheduleEvents();
}

function _parseAuditScheduleJson(jsonCompleto) {
  if (!jsonCompleto) return null;
  if (typeof jsonCompleto !== "string") return jsonCompleto;

  try {
    return JSON.parse(jsonCompleto);
  } catch (error) {
    try {
      return JSON.parse(jsonCompleto.replace(/'/g, '"'));
    } catch {
      throw error;
    }
  }
}

function _normalizeAuditScheduleData(raw) {
  const data = raw || {};
  const events = _extractAuditScheduleEvents(data)
    .map(_normalizeAuditScheduleEvent)
    .filter(event => event.project && event.gate && event.monthKey)
    .sort((a, b) => a.monthKey.localeCompare(b.monthKey) || a.gate.localeCompare(b.gate) || a.project.localeCompare(b.project));

  const monthMap = new Map();
  _extractAuditScheduleMonths(data).forEach(month => {
    const key = month.key || _buildAuditScheduleMonthKey(month.year, month.month);
    if (!key) return;
    monthMap.set(key, {
      key,
      label: month.label || month.monthName || _formatAuditScheduleMonthKey(key),
      monthName: month.monthName || month.label || _formatAuditScheduleMonthKey(key),
      month: Number(month.month || key.slice(5, 7)),
      year: Number(month.year || key.slice(0, 4)),
      endDate: month.endDate || _monthEndDate(Number(month.year || key.slice(0, 4)), Number(month.month || key.slice(5, 7))),
      events: []
    });
  });

  events.forEach(event => {
    if (!monthMap.has(event.monthKey)) {
      monthMap.set(event.monthKey, {
        key: event.monthKey,
        label: event.monthLabel || _formatAuditScheduleMonthKey(event.monthKey),
        monthName: event.monthName || event.monthLabel || _formatAuditScheduleMonthKey(event.monthKey),
        month: Number(event.monthKey.slice(5, 7)),
        year: Number(event.monthKey.slice(0, 4)),
        endDate: event.deadline || _monthEndDate(Number(event.monthKey.slice(0, 4)), Number(event.monthKey.slice(5, 7))),
        events: []
      });
    }
    monthMap.get(event.monthKey).events.push(event);
  });

  const months = [...monthMap.values()]
    .filter(month => month.events.length || _extractAuditScheduleMonths(data).length)
    .sort((a, b) => a.key.localeCompare(b.key));

  const projectCount = new Set(events.map(event => _normalizeAuditScheduleSearch(event.project))).size;

  return {
    version: data.version || "audit-schedule-v1",
    generatedAt: data.generatedAt || data.lastGlobalUpdate || "",
    metadata: data.metadata || {},
    summary: {
      totalEvents: events.length,
      auditEvents: events.filter(event => event.isAuditGate).length,
      milestoneEvents: events.filter(event => !event.isAuditGate).length,
      projects: projectCount,
      months: months.length,
      ...(data.summary || {})
    },
    months,
    events
  };
}

function _extractAuditScheduleEvents(data) {
  if (!data) return [];
  if (Array.isArray(data)) return data;
  if (Array.isArray(data.events) && data.events.length) return data.events;
  if (Array.isArray(data.eventos) && data.eventos.length) return data.eventos;
  if (Array.isArray(data.months)) {
    return data.months.flatMap(month =>
      (month.events || month.eventos || []).map(event => ({
        ...event,
        monthKey: event.monthKey || month.key,
        monthLabel: event.monthLabel || month.label || month.monthName,
        monthName: event.monthName || month.monthName || month.label,
        month: event.month || month.month,
        year: event.year || month.year,
        deadline: event.deadline || month.endDate
      }))
    );
  }
  return [];
}

function _extractAuditScheduleMonths(data) {
  return Array.isArray(data?.months) ? data.months : [];
}

function _normalizeAuditScheduleEvent(event) {
  const year = Number(event.year || (event.monthKey ? String(event.monthKey).slice(0, 4) : 0));
  const month = Number(event.month || (event.monthKey ? String(event.monthKey).slice(5, 7) : 0));
  const monthKey = event.monthKey || _buildAuditScheduleMonthKey(year, month);
  const gate = String(event.gate || event.Gate || event.milestone || "").trim().toUpperCase();
  const project = String(event.project || event.projeto || event.name || "").trim();
  const isAuditGate = event.isAuditGate === true || AUDIT_SCHEDULE_GATES.includes(gate);
  const deadline = event.deadline || event.endDate || _monthEndDate(year, month);

  return {
    id: event.id || _auditScheduleEventId(monthKey, gate, project),
    project,
    gate,
    isAuditGate,
    deadline,
    monthKey,
    month,
    year,
    monthLabel: event.monthLabel || event.monthName || _formatAuditScheduleMonthKey(monthKey),
    monthName: event.monthName || event.monthLabel || _formatAuditScheduleMonthKey(monthKey),
    sourceRow: event.sourceRow || "",
    sourceGateColumn: event.sourceGateColumn || "",
    sourceProjectColumn: event.sourceProjectColumn || ""
  };
}

function _filterAuditScheduleData(data) {
  const filters = auditScheduleState.filters;
  const term = _normalizeAuditScheduleSearch(filters.search);
  const selectedMonths = _auditScheduleFilterValues(filters.months || filters.month);
  const selectedGates = _auditScheduleFilterValues(filters.gates || filters.gate);
  const selectedYears = _auditScheduleFilterValues(filters.years || filters.year || ["2026"]);

  const events = data.events.filter(event => {
    if (selectedMonths.length && !selectedMonths.includes(event.monthKey)) return false;
    if (selectedGates.length && !selectedGates.includes(event.gate)) return false;
    if (selectedYears.length && !selectedYears.includes(String(event.year || ""))) return false;
    if (!term) return true;
    return _normalizeAuditScheduleSearch(`${event.project} ${event.gate} ${event.monthLabel}`).includes(term);
  });

  const visibleKeys = new Set(events.map(event => event.monthKey));
  const months = data.months
    .filter(month => visibleKeys.has(month.key))
    .map(month => ({
      ...month,
      events: events.filter(event => event.monthKey === month.key)
    }));

  return { ...data, months, events };
}

function _auditScheduleFilterValues(value) {
  if (!value || value === "all") return [];
  if (Array.isArray(value)) return value.map(String).filter(Boolean);
  return [String(value)].filter(Boolean);
}

function _renderAuditScheduleHeader(data, visibleEvents = null) {
  const metadata = data?.metadata || {};
  const generated = _formatAuditScheduleDateTime(data?.generatedAt);
  const source = metadata.sourceBlock || "Planilha PMO";
  const year = metadata.year || (data?.months?.[0]?.year) || "Not loaded";
  const countedEvents = Array.isArray(visibleEvents) ? visibleEvents : data?.events || [];
  const statusCounts = _auditScheduleStatusCounts(countedEvents);

  return `
    <section class="audit-schedule-header">
      <div class="audit-schedule-heading">
        <span class="audit-schedule-eyebrow">Audit Schedule</span>
        <h2>PMO Gate Calendar</h2>
        <p>Horizontal planning view for gate passages. MS0, MS1, QG2 and QG3 are emphasized as audit deadlines.</p>
        <div class="audit-schedule-meta">
          <span>Source: ${_escapeAuditSchedule(source)}</span>
          <span>Year: ${_escapeAuditSchedule(year)}</span>
          <span>Updated: ${_escapeAuditSchedule(generated)}</span>
        </div>
      </div>
      <div class="audit-schedule-live-grid">
        <div class="audit-schedule-live-card overdue">
          <span>Overdue audits</span>
          <strong>${statusCounts.overdue}</strong>
          <small>month closed, gate not reached</small>
        </div>
        <div class="audit-schedule-live-card">
          <span>Due within 30 days</span>
          <strong>${statusCounts.soon}</strong>
          <small>audit deadline approaching</small>
        </div>
      </div>
      <button class="audit-schedule-refresh-btn" id="auditScheduleRefreshBtn" type="button">
        <i class="fa-solid fa-rotate-right"></i>
        Refresh
      </button>
    </section>
  `;
}

function _auditScheduleStatusCounts(events) {
  const counts = { overdue: 0, soon: 0 };

  events.forEach(event => {
    if (!event.isAuditGate) return;
    const status = _auditScheduleEventStatus(event);
    if (status === "overdue") counts.overdue += 1;
    if (status === "soon") counts.soon += 1;
  });

  return counts;
}

function _renderAuditScheduleLoading() {
  if (typeof renderSiteLoading === "function") {
    return renderSiteLoading("Loading audit schedule", "Fetching PMO gate planning from Firestore.");
  }
  return `<div class="site-loading" data-loading="true"><strong>Loading audit schedule</strong></div>`;
}

function _renderAuditScheduleControls(data) {
  const filters = auditScheduleState.filters;
  const months = data.months.map(month => [month.key, `${month.monthName} ${month.year}`]);
  const gates = [...new Set(data.events.map(event => event.gate).filter(Boolean))].sort(_sortAuditScheduleGates);
  const years = [...new Set([2026, ...data.events.map(event => event.year).filter(Boolean)])]
    .map(String)
    .sort();

  filters.months = _auditScheduleFilterValues(filters.months || filters.month)
    .filter(value => months.some(([key]) => key === value));
  filters.gates = _auditScheduleFilterValues(filters.gates || filters.gate)
    .filter(value => gates.includes(value));
  filters.years = _auditScheduleFilterValues(filters.years || filters.year || ["2026"])
    .filter(value => years.includes(value));
  if (!filters.years.length && years.includes("2026")) filters.years = ["2026"];

  return `
    <section class="audit-schedule-controls">
      <label class="audit-schedule-control audit-schedule-search">
        <i class="fa-solid fa-magnifying-glass"></i>
        <input id="auditScheduleSearch" type="search" placeholder="Search project, gate or month..." value="${_escapeAuditSchedule(filters.search)}">
      </label>
      ${_renderAuditScheduleMultiFilter("auditScheduleMonths", "fa-calendar-days", "Months", filters.months, months)}
      ${_renderAuditScheduleMultiFilter("auditScheduleGates", "fa-code-branch", "Gates", filters.gates, gates.map(gate => [gate, gate]))}
      ${_renderAuditScheduleMultiFilter("auditScheduleYears", "fa-calendar", "Year", filters.years, years.map(item => [item, item]), true)}
    </section>
  `;
}

function _renderAuditScheduleMultiFilter(id, icon, label, selected, options, single = false) {
  const selectedSet = new Set(selected);
  const caption = selected.length
    ? selected.map(value => options.find(([optionValue]) => optionValue === value)?.[1] || value).join(", ")
    : `All ${label.toLowerCase()}`;

  return `
    <div class="audit-schedule-control audit-schedule-multi" data-filter-id="${id}" data-single="${single ? "true" : "false"}">
      <i class="fa-solid ${icon}"></i>
      <button class="audit-schedule-multi-btn" type="button" id="${id}Btn">
        <span>${_escapeAuditSchedule(caption)}</span>
        <i class="fa-solid fa-chevron-down"></i>
      </button>
      <div class="audit-schedule-multi-menu" id="${id}Menu">
        ${options.map(([optionValue, optionLabel]) => `
          <label>
            <input
              type="checkbox"
              value="${_escapeAuditSchedule(optionValue)}"
              ${selectedSet.has(String(optionValue)) ? "checked" : ""}
            >
            <span>${_escapeAuditSchedule(optionLabel)}</span>
          </label>
        `).join("")}
      </div>
    </div>
  `;
}

function _renderAuditScheduleCalendar(months) {
  if (!months.length) {
    return _renderAuditScheduleEmpty("No events for current filters.", true);
  }

  return `
    <section class="audit-schedule-calendar-board">
      <div class="audit-schedule-calendar-head">
        <div>
          <h3>Calendar</h3>
          <p>Each lane is a PMO month. Audit gates carry the audit deadline at month end.</p>
        </div>
        <span>${months.reduce((sum, month) => sum + month.events.length, 0)} visible event(s)</span>
      </div>
      <div class="audit-schedule-calendar-scroll">
        ${months.map(_renderAuditScheduleMonthLane).join("")}
      </div>
    </section>
  `;
}

function _renderAuditScheduleMonthLane(month) {
  const auditEvents = month.events.filter(event => event.isAuditGate);
  const milestoneEvents = month.events.filter(event => !event.isAuditGate);

  return `
    <article class="audit-schedule-month-lane">
      <header>
        <div>
          <strong>${_escapeAuditSchedule(month.monthName)}</strong>
          <span>${_escapeAuditSchedule(month.year)}</span>
        </div>
        <small>${_escapeAuditSchedule(_formatAuditScheduleDate(month.endDate))}</small>
      </header>
      <div class="audit-schedule-month-summary">
        <span>${month.events.length} gate(s)</span>
        <span>${auditEvents.length} audit required</span>
        ${milestoneEvents.length ? `<span>${milestoneEvents.length} gate only</span>` : ""}
      </div>
      <div class="audit-schedule-month-events">
        <section class="audit-schedule-month-section audit-required">
          <div class="audit-schedule-month-section-title">
            <span>Audit required</span>
            <strong>${auditEvents.length}</strong>
          </div>
          ${auditEvents.length ? auditEvents.map(_renderAuditScheduleEvent).join("") : _renderAuditScheduleEmpty("No audit gates this month.", true)}
        </section>
        <section class="audit-schedule-month-section gate-only">
          <div class="audit-schedule-month-section-title">
            <span>Gate passage only</span>
            <strong>${milestoneEvents.length}</strong>
          </div>
          ${milestoneEvents.length ? milestoneEvents.map(_renderAuditScheduleEvent).join("") : _renderAuditScheduleEmpty("No non-audit gates.", true)}
        </section>
      </div>
    </article>
  `;
}

function _renderAuditScheduleEvent(event) {
  const status = _auditScheduleEventStatus(event);
  const tag = event.isAuditGate ? "button" : "article";
  const actionAttrs = event.isAuditGate
    ? `type="button" data-project="${_escapeAuditSchedule(event.project)}" data-gate="${_escapeAuditSchedule(event.gate)}" data-month="${_escapeAuditSchedule(event.monthKey)}"`
    : "";
  const statusText = event.isAuditGate
    ? _auditScheduleStatusLabel(event, status)
    : "Gate passage only";

  return `
    <${tag} class="audit-schedule-event ${event.isAuditGate ? "audit" : "milestone"} ${status}" ${actionAttrs}>
      <div class="audit-schedule-gate-badge">${_escapeAuditSchedule(event.gate)}</div>
      <div class="audit-schedule-event-body">
        <strong title="${_escapeAuditSchedule(event.project)}">${_escapeAuditSchedule(event.project)}</strong>
        <span>${_escapeAuditSchedule(statusText)} - ${_escapeAuditSchedule(_formatAuditScheduleDate(event.deadline))}</span>
      </div>
      <i class="fa-solid ${_auditScheduleStatusIcon(event, status)}"></i>
    </${tag}>
  `;
}

function _renderAuditScheduleQueueStrip(events) {
  const sorted = [...events]
    .sort((a, b) => _parseAuditScheduleDate(a.deadline) - _parseAuditScheduleDate(b.deadline))
    .slice(0, 12);

  return `
    <section class="audit-schedule-queue-strip">
      <div class="audit-schedule-queue-heading">
        <div>
          <h3>Audit Queue</h3>
          <p>Closest audit gates by month-end deadline.</p>
        </div>
        <span>${events.length} audit gate(s)</span>
      </div>
      <div class="audit-schedule-queue-list">
        ${sorted.length ? sorted.map(event => `
          <div class="audit-schedule-queue-item ${_auditScheduleEventStatus(event)}">
            <span>${_escapeAuditSchedule(event.gate)}</span>
            <strong title="${_escapeAuditSchedule(event.project)}">${_escapeAuditSchedule(event.project)}</strong>
            <small>${_escapeAuditSchedule(_formatAuditScheduleDate(event.deadline))}</small>
          </div>
        `).join("") : _renderAuditScheduleEmpty("No audit gates in current filters.", true)}
      </div>
    </section>
  `;
}

function _renderAuditScheduleGateMix(events) {
  const rows = [...new Set(events.map(event => event.gate))]
    .sort(_sortAuditScheduleGates)
    .map(gate => ({
      gate,
      count: events.filter(event => event.gate === gate).length,
      audit: AUDIT_SCHEDULE_GATES.includes(gate)
    }));
  const max = Math.max(...rows.map(row => row.count), 1);

  return `
    <section class="audit-schedule-panel audit-schedule-mix">
      <div class="audit-schedule-panel-header compact">
        <h3>Gate Mix</h3>
      </div>
      <div class="audit-schedule-mix-list">
        ${rows.length ? rows.map(row => `
          <div class="audit-schedule-mix-row ${row.audit ? "audit" : ""}">
            <span>${_escapeAuditSchedule(row.gate)}</span>
            <div><em style="width:${Math.max(10, (row.count / max) * 100)}%"></em></div>
            <strong>${row.count}</strong>
          </div>
        `).join("") : _renderAuditScheduleEmpty("No gates to summarize.", true)}
      </div>
    </section>
  `;
}

function _renderAuditScheduleEmpty(message, compact = false) {
  return `
    <div class="audit-schedule-empty ${compact ? "compact" : ""}">
      <i class="fa-regular fa-circle-check"></i>
      <span>${_escapeAuditSchedule(message)}</span>
    </div>
  `;
}

function _bindAuditScheduleEvents() {
  const refresh = document.getElementById("auditScheduleRefreshBtn");
  if (refresh) refresh.addEventListener("click", () => carregarAuditSchedule(true));

  const search = document.getElementById("auditScheduleSearch");
  if (search) search.addEventListener("input", event => {
    auditScheduleState.filters.search = event.target.value;
    _renderAuditScheduleFromTyping();
    const nextSearch = document.getElementById("auditScheduleSearch");
    if (nextSearch) {
      nextSearch.focus();
      nextSearch.setSelectionRange(nextSearch.value.length, nextSearch.value.length);
    }
  });

  [
    ["auditScheduleMonths", "months"],
    ["auditScheduleGates", "gates"],
    ["auditScheduleYears", "years"]
  ].forEach(([id, key]) => {
    const root = document.querySelector(`[data-filter-id="${id}"]`);
    const button = document.getElementById(`${id}Btn`);
    const menu = document.getElementById(`${id}Menu`);
    if (!root || !button || !menu) return;

    button.addEventListener("click", event => {
      event.stopPropagation();
      document.querySelectorAll(".audit-schedule-multi.open").forEach(item => {
        if (item !== root) item.classList.remove("open");
      });
      root.classList.toggle("open");
      auditScheduleState.openFilter = root.classList.contains("open") ? id : null;
    });

    menu.querySelectorAll("input[type='checkbox']").forEach(input => {
      input.addEventListener("change", event => {
        if (root.dataset.single === "true" && event.target.checked) {
          menu.querySelectorAll("input[type='checkbox']").forEach(other => {
            if (other !== event.target) other.checked = false;
          });
        }

        auditScheduleState.filters[key] = [...menu.querySelectorAll("input[type='checkbox']:checked")]
          .map(item => item.value);
        auditScheduleState.openFilter = id;
        renderAuditSchedule();
      });
    });

    if (auditScheduleState.openFilter === id) {
      root.classList.add("open");
    }
  });

  document.removeEventListener("click", _closeAuditScheduleMultiFilters);
  document.addEventListener("click", _closeAuditScheduleMultiFilters);

  _bindAuditScheduleDragScroll();
}

function _closeAuditScheduleMultiFilters(event) {
  if (event.target.closest(".audit-schedule-multi")) return;
  document.querySelectorAll(".audit-schedule-multi.open").forEach(item => item.classList.remove("open"));
  auditScheduleState.openFilter = null;
}

function _renderAuditScheduleFromTyping() {
  auditScheduleState.quietRender = true;
  renderAuditSchedule();

  window.requestAnimationFrame(() => {
    auditScheduleState.quietRender = false;
  });
}

function _auditScheduleEventStatus(event) {
  if (!event.isAuditGate) return "neutral";
  if (_isAuditScheduleGateCompleted(event)) return "completed";
  if (_isAuditScheduleOverdue(event)) return "overdue";
  const deadline = _parseAuditScheduleDate(event.deadline);
  const today = _startOfToday();
  const days = Math.ceil((deadline - today) / 86400000);
  if (days <= 30) return "soon";
  return "future";
}

function _isAuditScheduleOverdue(event) {
  return event.isAuditGate && _parseAuditScheduleDate(event.deadline) < _startOfToday();
}

function _auditScheduleStatusLabel(event, status) {
  if (!event.isAuditGate) return "Gate passage only";
  if (status === "completed") return "Audit completed";
  if (status === "overdue") return "Audit overdue";
  if (status === "soon") return "Audit approaching";
  return "Planned audit";
}

function _auditScheduleStatusIcon(event, status) {
  if (!event.isAuditGate) return "fa-flag";
  if (status === "completed") return "fa-circle-check";
  if (status === "overdue") return "fa-triangle-exclamation";
  if (status === "soon") return "fa-clock";
  return "fa-calendar-check";
}

function _isAuditScheduleGateCompleted(event) {
  const audit = _getScheduleAuditRecord(event);
  const currentGate = String(audit?.gates?.current || audit?.actualGate || audit?.currentGate || "").trim().toUpperCase();
  return _auditScheduleGateProgress(currentGate) >= _auditScheduleGateProgress(event.gate);
}

function _getScheduleAuditRecord(event) {
  if (typeof auditState === "undefined" || !auditState.byProjectId) return null;

  const directId = normalizarAuditProjectId(event.projectId || event.id);
  if (directId && auditState.byProjectId.has(directId)) {
    return auditState.byProjectId.get(directId);
  }

  const eventName = event.project;
  const eventKey = _normalizeAuditScheduleSearch(eventName);
  const eventTokens = _auditScheduleProjectTokens(eventName);

  return [...auditState.byProjectId.values()].find(audit => {
    const names = [
      audit.projectName,
      audit.nome,
      audit.backlogProjectName,
      audit.dashboardName,
      audit.id,
      audit.projectId,
      audit.canonicalId,
      ...(Array.isArray(audit.aliases) ? audit.aliases : [])
    ].filter(Boolean);

    return names.some(name => {
      const key = _normalizeAuditScheduleSearch(name);
      if (!key || key === eventKey) return key === eventKey;
      if (typeof _nomesProjetosEquivalentes === "function" && _nomesProjetosEquivalentes(eventName, name)) return true;

      const auditTokens = _auditScheduleProjectTokens(name);
      const shorter = eventTokens.length <= auditTokens.length ? eventTokens : auditTokens;
      const longer = eventTokens.length <= auditTokens.length ? auditTokens : eventTokens;
      if (shorter.length < 2) return false;

      const matches = shorter.filter(token => longer.includes(token)).length;
      return matches >= Math.max(2, Math.ceil(shorter.length * 0.72));
    });
  }) || null;
}

function _auditScheduleGateProgress(gate) {
  const order = ["MS0", "QG1", "MS1", "QG2", "MS2", "QG3", "QG4"];
  const index = order.indexOf(String(gate || "").trim().toUpperCase());
  return index === -1 ? -1 : index;
}

function _auditScheduleProjectTokens(value) {
  return _normalizeAuditScheduleSearch(value)
    .split(/[^a-z0-9]+/i)
    .filter(token => token.length >= 2)
    .filter(token => !["oes", "oem", "the", "and", "for", "com", "sem"].includes(token));
}

function _openAuditFromScheduleEvent(projectName, gate) {
  const event = {
    project: projectName,
    gate
  };
  const open = () => {
    const audit = _getScheduleAuditRecord(event);
    const canonicalId = audit?.canonicalId || normalizarAuditProjectId(audit?.id || projectName);
    abrirAuditoria(canonicalId, audit?.projectName || projectName, "Audit Schedule", {
      canonicalId,
      source: "audit-schedule",
      gate
    });
  };

  if (typeof carregarAudits === "function" && !auditState.loaded) {
    if (!auditState.loading) carregarAudits();
    window.setTimeout(() => _openAuditFromScheduleEvent(projectName, gate), 150);
    return;
  }

  open();
}

function _bindAuditScheduleDragScroll() {
  const scroller = document.querySelector(".audit-schedule-calendar-scroll");
  if (!scroller) return;

  let active = false;
  let moved = false;
  let startX = 0;
  let startScroll = 0;

  scroller.addEventListener("click", event => {
    if (scroller.dataset.suppressClick === "true") {
      event.preventDefault();
      event.stopPropagation();
      scroller.dataset.suppressClick = "";
      return;
    }

    const auditCard = event.target.closest(".audit-schedule-event.audit");
    if (auditCard) {
      event.preventDefault();
      event.stopPropagation();
      _openAuditFromScheduleEvent(auditCard.dataset.project, auditCard.dataset.gate);
    }
  }, true);

  scroller.addEventListener("pointerdown", event => {
    if (event.button !== 0) return;
    if (event.target.closest(".audit-schedule-event.audit")) return;
    active = true;
    moved = false;
    startX = event.clientX;
    startScroll = scroller.scrollLeft;
    scroller.classList.add("is-dragging");
  });

  scroller.addEventListener("pointermove", event => {
    if (!active) return;
    const delta = event.clientX - startX;
    if (Math.abs(delta) > 4) moved = true;
    scroller.scrollLeft = startScroll - delta;
  });

  const finish = event => {
    if (!active) return;
    active = false;
    if (moved) {
      scroller.dataset.suppressClick = "true";
      window.setTimeout(() => {
        if (scroller.dataset.suppressClick === "true") scroller.dataset.suppressClick = "";
      }, 0);
    }
    scroller.classList.remove("is-dragging");
  };

  scroller.addEventListener("pointerup", finish);
  scroller.addEventListener("pointercancel", finish);
}

function _sortAuditScheduleGates(a, b) {
  const order = ["MS0", "QG1", "MS1", "QG2", "MS2", "QG3", "QG4"];
  const ai = order.indexOf(a);
  const bi = order.indexOf(b);
  if (ai !== -1 || bi !== -1) return (ai === -1 ? 99 : ai) - (bi === -1 ? 99 : bi);
  return String(a).localeCompare(String(b));
}

function _auditScheduleEventId(monthKey, gate, project) {
  return _normalizeAuditScheduleSearch(`${monthKey}-${gate}-${project}`)
    .replace(/\s+/g, "-")
    .replace(/[^a-z0-9-]/g, "")
    .replace(/-+/g, "-")
    .replace(/^-|-$/g, "");
}

function _buildAuditScheduleMonthKey(year, month) {
  if (!year || !month) return "";
  return `${String(year).padStart(4, "0")}-${String(month).padStart(2, "0")}`;
}

function _monthEndDate(year, month) {
  if (!year || !month) return "";
  const date = new Date(year, month, 0);
  return `${date.getFullYear()}-${String(date.getMonth() + 1).padStart(2, "0")}-${String(date.getDate()).padStart(2, "0")}`;
}

function _parseAuditScheduleDate(value) {
  if (!value) return new Date(0);
  if (value instanceof Date) return value;
  const date = new Date(`${String(value).slice(0, 10)}T00:00:00`);
  return Number.isNaN(date.getTime()) ? new Date(0) : date;
}

function _startOfToday() {
  const today = new Date();
  today.setHours(0, 0, 0, 0);
  return today;
}

function _formatAuditScheduleDate(value) {
  const date = _parseAuditScheduleDate(value);
  if (!date.getTime()) return "Not defined";
  return date.toLocaleDateString("en-US", { month: "short", day: "2-digit", year: "numeric" });
}

function _formatAuditScheduleDateTime(value) {
  if (!value) return "Not loaded";
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return String(value);
  return date.toLocaleString("en-US", {
    month: "short",
    day: "2-digit",
    year: "numeric",
    hour: "2-digit",
    minute: "2-digit"
  });
}

function _formatAuditScheduleMonthKey(key) {
  if (!key) return "";
  const date = _parseAuditScheduleDate(`${key}-01`);
  if (!date.getTime()) return key;
  return date.toLocaleDateString("en-US", { month: "short" });
}

function _normalizeAuditScheduleSearch(value) {
  return String(value || "")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .trim();
}

function _escapeAuditSchedule(value) {
  return String(value ?? "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");
}
